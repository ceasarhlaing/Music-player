for (let i = 0; i < albums.length; i++) {

    const liTag = document.createElement('li')
    liTag.classList.add('album')

    const imgTag = document.createElement('img')
    imgTag.src = albums[i].cover

    const contentTag = document.createElement('div')

    const nameTag = document.createElement('div')
    nameTag.classList.add('album_name')
    nameTag.textContent = albums[i].name

    const singerTag = document.createElement('div')
    singerTag.classList.add('album_singer')
    singerTag.textContent = albums[i].singer

    contentTag.append(nameTag, singerTag)

    liTag.append(imgTag, contentTag)

    albumListTag.append(liTag)


    liTag.addEventListener('click', playAlbum.bind(null, albums[i].id))
}
